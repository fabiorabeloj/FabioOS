const now = () => new Date().toISOString();

function policy(
  partial: Omit<AgentPolicy, "riskLevel" | "riskNotes">,
): AgentPolicy {
  return { ...partial, riskLevel: "safe", riskNotes: [] };
}

function p(
  sandboxMode: AgentPolicy["sandboxMode"],
  workspaceAccess: AgentPolicy["workspaceAccess"],
  allowed: string[],
  denied: string[],
  elevated: AgentPolicy["elevated"] = "disabled",
): Omit<AgentPolicy, "riskLevel" | "riskNotes"> {
  return {
    sandboxMode,
    sandboxScope: "agent",
    workspaceRoot: "~/.openclaw/sandbox",
    workspaceAccess,
    dockerSandbox: sandboxMode === "all" ? "enabled" : "disabled",
    browserSandbox: allowed.includes("browser") ? "isolated" : "disabled",
    prunePolicy: "on-idle",
    allowedTools: allowed,
    deniedTools: denied,
    toolProfile: "minimal",
    providerPolicy: "inherit",
    sandboxToolPolicy: "inherit",
    subagentToolPolicy: "deny-all",
    elevated,
    agentDir: "",
    authProfile: "local",
  };
}

function def(
  id: string,
  name: string,
  layer: AgentLayer,
  role: string,
  responsibilities: string[],
  inputs: string[],
  outputs: string[],
  status: AgentStatus,
  catalogZone: string,
  mapZone: Zone,
  requiresApprovalFor: string[],
  policyPartial: Omit<AgentPolicy, "riskLevel" | "riskNotes">,
  essential: boolean,
  initialTask?: string,
): FabioAgentDefinition {
  return {
    id,
    name,
    layer,
    role,
    responsibilities,
    inputs,
    outputs,
    status,
    catalogZone,
    mapZone,
    requiresApprovalFor,
    essential,
    initialTask: initialTask ?? (status === "planned" ? "Planejado — nao implantado" : "Inativo"),
    policyTemplate: {
      ...policyPartial,
      agentDir: policyPartial.agentDir || `~/.openclaw/agents/${id}/agent`,
    },
  };
}

