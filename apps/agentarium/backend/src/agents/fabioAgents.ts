export const ACTIVE_AGENT_IDS = FABIO_AGENT_DEFINITIONS.filter(
  (d) => d.status === "active",
).map((d) => d.id);

export function getDefinition(id: string): FabioAgentDefinition | undefined {
  return FABIO_AGENT_DEFINITIONS.find((d) => d.id === id);
}

export function definitionToAgent(
  d: FabioAgentDefinition,
  overrides?: Partial<Pick<Agent, "state" | "task" | "zone" | "updatedAt">>,
): Agent {
  const base: Agent = {
    id: d.id,
    name: d.name,
    layer: d.layer,
    status: d.status,
    role: d.role,
    responsibilities: d.responsibilities,
    inputs: d.inputs,
    outputs: d.outputs,
    requiresApprovalFor: d.requiresApprovalFor,
    essential: d.essential,
    catalogZone: d.catalogZone,
    state: (overrides?.state ?? "idle") as AgentRuntimeState,
    task: overrides?.task ?? d.initialTask,
    zone: overrides?.zone ?? d.mapZone,
    updatedAt: overrides?.updatedAt ?? now(),
    policy: policy({ ...d.policyTemplate, agentDir: d.policyTemplate.agentDir }),
  };
  return refreshAgentRisk(base);
}

export function buildInitialActiveAgents(): Agent[] {
  return FABIO_AGENT_DEFINITIONS.filter((d) => d.status === "active").map((d) =>
    definitionToAgent(d, {
      state: "idle",
      task: d.initialTask,
      zone: d.mapZone,
    }),
  );
}

export function buildFullCatalog(liveAgents: Map<string, Agent>): Agent[] {
  return FABIO_AGENT_DEFINITIONS.map((d) => {
    const live = liveAgents.get(d.id);
    if (live && d.status === "active") return refreshAgentRisk(live);
    return definitionToAgent(d);
  }).sort((a, b) => a.name.localeCompare(b.name, "pt-BR"));
}

